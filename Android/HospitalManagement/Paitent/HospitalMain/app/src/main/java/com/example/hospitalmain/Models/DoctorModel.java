package com.example.hospitalmain.Models;

public class DoctorModel
{
    String DR_Id;
    String C_ID;
    String DR_Name;
    String DR_Email;
    String DR_Contact;
    String DR_Password;
    String DR_Fee;
    String DR_Status;
    String DR_Date;
    String DR_Img_Name;

    public DoctorModel()
    {

    }

    public String getDR_Id() {
        return DR_Id;
    }

    public void setDR_Id(String DR_Id) {
        this.DR_Id = DR_Id;
    }

    public String getC_ID() {
        return C_ID;
    }

    public void setC_ID(String c_ID) {
        C_ID = c_ID;
    }

    public String getDR_Name() {
        return DR_Name;
    }

    public void setDR_Name(String DR_Name) {
        this.DR_Name = DR_Name;
    }

    public String getDR_Email() {
        return DR_Email;
    }

    public void setDR_Email(String DR_Email) {
        this.DR_Email = DR_Email;
    }

    public String getDR_Contact() {
        return DR_Contact;
    }

    public void setDR_Contact(String DR_Contact) {
        this.DR_Contact = DR_Contact;
    }

    public String getDR_Password() {
        return DR_Password;
    }

    public void setDR_Password(String DR_Password) {
        this.DR_Password = DR_Password;
    }

    public String getDR_Fee() {
        return DR_Fee;
    }

    public void setDR_Fee(String DR_Fee) {
        this.DR_Fee = DR_Fee;
    }

    public String getDR_Status() {
        return DR_Status;
    }

    public void setDR_Status(String DR_Status) {
        this.DR_Status = DR_Status;
    }

    public String getDR_Date() {
        return DR_Date;
    }

    public void setDR_Date(String DR_Date) {
        this.DR_Date = DR_Date;
    }

    public String getDR_Img_Name() {
        return DR_Img_Name;
    }

    public void setDR_Img_Name(String DR_Img_Name) {
        this.DR_Img_Name = DR_Img_Name;
    }
}
